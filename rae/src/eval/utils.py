def to_torch_tensor(np_array):
    """
    Converts a NumPy array of shape (B, H, W, C) to a (B, C, H, W) float32 Torch tensor.
    Expects image to be in [0, 1] or [0, 255].
    Adjust if necessary.
    """
    tensor = torch.from_numpy(np_array).permute(0, 3, 1, 2)
    
    # If your data is in [0, 255], convert to [0, 1].
    if tensor.max() > 1.0:
        tensor = tensor.float() / 255.0
    else:
        tensor = tensor.float()
    return tensor
